package com.jersey.model;

public class User {
    private String name;
    private String password;
    
    public String getName() {
    	return name;
    }
    
    public String getPassword() {
    	return password;
    }
    
    public void setName(String name) {
    	this.name = name;
    }
    
    public void setPassword(String pass) {
    	this.password = pass;
    }
    
}
